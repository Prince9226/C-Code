#include <stdio.h>

int main() {
    int a = 10;     // signed integer
    printf("%d \n", a);

    long b = 20;    // long integar
    printf("%ld \n", b);

    unsigned c = 30;     // unsigned integer
    printf("%u \n", c);

    long long d = 40;     // long integar
    printf("%lld \n", d);

    return 0;
}
